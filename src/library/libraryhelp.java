    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model=(DefaultTableModel)t.getModel();
        SimpleDateFormat dtf = new SimpleDateFormat("yyyy-MM-dd");
        String d=dtf.format(j1.getDate());
        String op[]=new String[1000];
        String scno[]=new String[1000];
        String name[]=new String[1000];
        int i=0;
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://lib-lnct.c47pjqtqhh6h.us-west-1.rds.amazonaws.com/library", "harry", "prabhat1");
            Statement stmt=con.createStatement();
            ResultSet rst;
            String q="select Scholar_No,Book1 from issue1 where date1='"+d+"';";
            rst=stmt.executeQuery(q);
            model.setRowCount(0);
            while(rst.next())
            {
                scno[i]=rst.getString(1);
                op[i]=rst.getString(2);
                i++;
            }
            q="select Scholar_No,Book2 from issue1 where date2='"+d+"';";
            rst=stmt.executeQuery(q);
            while(rst.next())
            {
                scno[i]=rst.getString(1);
                op[i]=rst.getString(2);
                i++;
            }
            int count=i;
            for(i=0;i<count;i++)
            {
            q="select Name from book where Book_id='"+op[i]+"';";
            rst=stmt.executeQuery(q);
            if(rst.next())
                name[i]=rst.getString("Name");
            }
            if(i!=0){
            for(i=0;i<count;i++)
           model.addRow(new Object[]{scno[i],name[i]});
            }
            else
            {
              JOptionPane.showMessageDialog(null, "No Record Found");
            }
            ll1.setText(""+count);
            stmt.close();
            con.close();
            rst.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage(),"Error",1);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

